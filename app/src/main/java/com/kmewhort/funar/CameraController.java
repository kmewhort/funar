    protected void startBackgroundThread() {
        mBackgroundThread = new HandlerThread("Camera Background");
        mBackgroundThread.start();
        mBackgroundHandler = new Handler(mBackgroundThread.getLooper());
    }

    protected void stopBackgroundThread() {
        mBackgroundThread.quitSafely();
        try {
            mBackgroundThread.join();
            mBackgroundThread = null;
            mBackgroundHandler = null;
        } catch (InterruptedException e) {
            e.printStackTrace();
        }
    }

    protected void takePicture() {
        if (null == cameraDevice) {
            Log.e(TAG, "cameraDevice is null");
            return;
        }

        CameraManager manager = (CameraManager) getSystemService(Context.CAMERA_SERVICE);
        try {
            CameraCharacteristics characteristics = manager.getCameraCharacteristics(cameraDevice.getId());

            // physical camera readers
            List<ImageReader> physicalImageReaders = new ArrayList<>();
            List<OutputConfiguration> outputConfigs = new ArrayList<>();
            List<ImageReader.OnImageAvailableListener> listeners = new ArrayList<>();
            List<Surface> surfaces = new ArrayList<>();

            // request builder to which to add the surfaces we're requesting
            CaptureRequest.Builder requestBuilder =
                    cameraDevice.createCaptureRequest(CameraDevice.TEMPLATE_RECORD); // TODO: is template preview right?

            // for each of the two physical devices
            for (int i = 0; i < 2; i++) {
                ImageReader reader = ImageReader.newInstance(
                        imageDimension.getWidth(),
                        imageDimension.getHeight(),
                        ImageFormat.YUV_420_888,
                        2
                );

                String physicalCameraId = mCameraSelector.physicalCameraId(i);
                OutputConfiguration config = new OutputConfiguration(reader.getSurface());
                config.setPhysicalCameraId(physicalCameraId);

                ImageReader.OnImageAvailableListener readerListener = new ImageReader.OnImageAvailableListener() {
                    @Override
                    public void onImageAvailable(ImageReader reader) {
                        // for each physical camera
                        for (int i = 0; i < 2; i++) {
                            Image img = null;
                            try {
                                img = reader.acquireLatestImage();


                                Mat rgbMat = StereoImageProcessor.Yuv420888toRGB(img);
                                //img.close();

                                // save the image
//                                File file = new File(Environment.getExternalStorageDirectory() + "/pic" + i + ".jpg");
//                                ByteBuffer buffer = img.getPlanes()[0].getBuffer();
//                                byte[] bytes = new byte[buffer.capacity()];
//                                buffer.get(bytes);
 //                               save(bytes);

                                // output the bitmap to the texture view
                                Bitmap imgBitmap = Bitmap.createBitmap(rgbMat.width(), rgbMat.height(), Bitmap.Config.ARGB_8888);
                                Utils.matToBitmap(rgbMat, imgBitmap);
                                Rect rc = new Rect();
                                Canvas c = textureView.lockCanvas(rc);
                                rc.set(0, 0, rgbMat.width(), rgbMat.height());
                                c.drawBitmap(imgBitmap, 0, 0, null);
                                textureView.unlockCanvasAndPost(c);
                                Thread.sleep(10000);
                            } catch (InterruptedException e) {
                                e.printStackTrace();
                            } finally {
                                if (img != null) {
                                    img.close();
                                }
                            }
                        }
                    }

                    private void save(byte[] bytes) throws IOException {
                        OutputStream output = null;
                        try {
                            output = new FileOutputStream(file);
                            output.write(bytes);
                        } finally {
                            if (null != output) {
                                output.close();
                            }
                        }
                    }
                };

                // TODO: shouldn't need two listeners?
                reader.setOnImageAvailableListener(readerListener, mBackgroundHandler);

                physicalImageReaders.add(reader);
                outputConfigs.add(config);
                listeners.add(readerListener);
                surfaces.add(reader.getSurface());

                requestBuilder.addTarget(reader.getSurface());
            } // end for each physical device


            // TODO: why do I need a capture listener (already set the image listener?)
            final CameraCaptureSession.CaptureCallback captureListener = new CameraCaptureSession.CaptureCallback() {
                @Override
                public void onCaptureCompleted(CameraCaptureSession session, CaptureRequest request, TotalCaptureResult result) {
                    super.onCaptureCompleted(session, request, result);
                    Toast.makeText(CameraController.this, "Saved:" + file, Toast.LENGTH_SHORT).show();
                    try {
                        Thread.sleep(3000);
                    } catch (InterruptedException e) {
                    } finally {
                        createCameraPreview();
                    }
                }
            };

            cameraDevice.createCaptureSession(surfaces, new CameraCaptureSession.StateCallback() {
                @Override
                public void onConfigured(CameraCaptureSession session) {
                    try {
                        session.capture(requestBuilder.build(), captureListener, mBackgroundHandler);
                    } catch (CameraAccessException e) {
                        e.printStackTrace();
                    }
                }

                @Override
                public void onConfigureFailed(CameraCaptureSession session) {
                }
            }, mBackgroundHandler);
        } catch (CameraAccessException e) {
            e.printStackTrace();
        }
    }


    protected void createCameraPreview() {
        try {
            SurfaceTexture texture = textureView.getSurfaceTexture();
            assert texture != null;
            texture.setDefaultBufferSize(imageDimension.getWidth(), imageDimension.getHeight());
            Surface surface = new Surface(texture);
            captureRequestBuilder = cameraDevice.createCaptureRequest(
                    CameraDevice.TEMPLATE_PREVIEW//,
                    // TEST: just the first
                    //new HashSet<>(Arrays.asList(mCameraSelector.physicalCameraId(0)))
            );
            captureRequestBuilder.addTarget(surface);
            cameraDevice.createCaptureSession(Arrays.asList(surface), new CameraCaptureSession.StateCallback() {
                @Override
                public void onConfigured(@NonNull CameraCaptureSession cameraCaptureSession) {
                    //The camera is already closed
                    if (null == cameraDevice) {
                        return;
                    }
                    // When the session is ready, we start displaying the preview.
                    cameraCaptureSessions = cameraCaptureSession;
                    updatePreview();
                }

                @Override
                public void onConfigureFailed(@NonNull CameraCaptureSession cameraCaptureSession) {
                    Toast.makeText(CameraController.this, "Configuration change", Toast.LENGTH_SHORT).show();
                }
            }, null);
        } catch (CameraAccessException e) {
            e.printStackTrace();
        }
    }

    private void openCamera() {
        CameraManager manager = (CameraManager) getSystemService(Context.CAMERA_SERVICE);
        Log.e(TAG, "is camera open");
        try {
            mCameraSelector = new CameraSelector(manager);
            CameraCharacteristics characteristics = manager.getCameraCharacteristics(mCameraSelector.stereoCameraId());
            StreamConfigurationMap map = characteristics.get(CameraCharacteristics.SCALER_STREAM_CONFIGURATION_MAP);
            assert map != null;
            imageDimension = map.getOutputSizes(SurfaceTexture.class)[0];
            // Add permission for camera and let user grant the permission
            if (ActivityCompat.checkSelfPermission(this, Manifest.permission.CAMERA) != PackageManager.PERMISSION_GRANTED && ActivityCompat.checkSelfPermission(this, Manifest.permission.WRITE_EXTERNAL_STORAGE) != PackageManager.PERMISSION_GRANTED) {
                ActivityCompat.requestPermissions(CameraController.this, new String[]{Manifest.permission.CAMERA, Manifest.permission.WRITE_EXTERNAL_STORAGE}, REQUEST_CAMERA_PERMISSION);
                return;
            }
            manager.openCamera(mCameraSelector.stereoCameraId(), stateCallback, null);
        } catch (CameraAccessException e) {
            e.printStackTrace();
        }
        Log.e(TAG, "openCamera X");
    }

